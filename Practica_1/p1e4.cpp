#include <iostream>

using namespace std;

int main()
{
    cout << "bool: " << sizeof(bool) << " bytes\n";
    cout << "char: " << sizeof(char) << " bytes\n";
    cout << "int: " << sizeof(int) << " bytes\n";
    cout << "unsigned: " << sizeof(unsigned) << " bytes\n";
    cout << "double: " << sizeof(double) << " bytes\n";

    return 0;
}
